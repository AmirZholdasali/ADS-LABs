void bfs(Node* root) {
    if (!root) return;            // если дерево пустое — выходим

    queue<Node*> q;               // создаём очередь
    q.push(root);                 // кладём корень в очередь

    while (!q.empty()) {          // пока очередь не пуста
        Node* cur = q.front();    // берём первый элемент
        q.pop();                  // удаляем его из очереди

        cout << cur->data << " "; // обрабатываем узел (печать)

        if (cur->left)            // если есть левый ребёнок
            q.push(cur->left);    // добавляем его в очередь
        if (cur->right)           // если есть правый ребёнок
            q.push(cur->right);   // добавляем его в очередь
    }
}
